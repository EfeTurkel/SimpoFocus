import Foundation
import Combine

enum AppLanguage: String, CaseIterable, Identifiable {
    case turkish = "tr"
    case english = "en"
    case german = "de"

    var id: String { rawValue }

    var displayName: String {
        switch self {
        case .turkish: return "Türkçe"
        case .english: return "English"
        case .german: return "Deutsch"
        }
    }

    var localeIdentifier: String { rawValue }

    static func defaultFromDevice() -> AppLanguage {
        let code = Locale.current.language.languageCode?.identifier.lowercased() ?? "tr"
        return AppLanguage(rawValue: code) ?? .turkish
    }
}

final class LocalizationManager: ObservableObject {
    static let shared = LocalizationManager()

    private let storageKey = "app_language"
    @Published var language: AppLanguage {
        didSet {
            UserDefaults.standard.set(language.rawValue, forKey: storageKey)
        }
    }

    private init() {
        if let stored = UserDefaults.standard.string(forKey: storageKey),
           let lang = AppLanguage(rawValue: stored) {
            language = lang
        } else {
            language = AppLanguage.defaultFromDevice()
        }
    }

    func translate(_ key: String, fallback: String? = nil, arguments: [CVarArg] = []) -> String {
        let template = translations[language]?[key] ?? fallback ?? key
        guard !arguments.isEmpty else { return template }
        let locale = Locale(identifier: language.localeIdentifier)
        return String(format: template, locale: locale, arguments: arguments)
    }

